  document.getElementById('site-search-btn').addEventListener('click', function() {
   const searchInput = document.getElementById('site-search');
  const searchBtn   = document.getElementById('site-search-btn');
  const blocks      = document.querySelectorAll('#main section, #main .icon-box, #main .portfolio-item');

  // Funcție care aplică sau resetează filtrul
  function applyFilter() {
    const query = searchInput.value.trim().toLowerCase();

    // Dacă nu există text, revenim la starea inițială
    if (!query) {
      blocks.forEach(block => block.style.display = '');
      return;
    }

    // Altfel, filtrăm
    blocks.forEach(block => {
      const text = block.textContent.toLowerCase();
      block.style.display = text.includes(query) ? '' : 'none';
    });
  }

  // Evenimente
  searchBtn.addEventListener('click', applyFilter);
  searchInput.addEventListener('input', applyFilter);
  });

  document.getElementById('contact-form').addEventListener('submit', function(e) {
    e.preventDefault();
    const form = this;
    const loading = form.querySelector('.loading');
    const errorMsg = form.querySelector('.error-message');
    const successMsg = form.querySelector('.sent-message');
  
    // Resetează mesajele
    errorMsg.style.display = 'none';
    successMsg.style.display = 'none';
  
    if (!form.checkValidity()) {
      // Browserul va afișa mesajele default de invalid
      form.reportValidity();
      return;
    }
  
    loading.style.display = 'block';
  
    // Trimite AJAX către formsubmit.co
    fetch(form.action, {
      method: form.method,
      body: new FormData(form),
    })
      .then(response => {
        loading.style.display = 'none';
        if (response.ok) {
          successMsg.style.display = 'block';
          form.reset();
        } else {
          throw new Error('Response not ok');
        }
      })
      .catch(error => {
        loading.style.display = 'none';
        errorMsg.textContent = 'A apărut o eroare. Încearcă din nou mai târziu.';
        errorMsg.style.display = 'block';
      });
  });
  